package org.example.strings;

public class TypeCasting {

    public static void main(String[] args) {

        int a = 5;
        double b = a;
        System.out.println(b);


        double m = 5.7;
        int c = (int) m;
        System.out.println(c);

        float f = 3.15F;

        int d = (int) f;
        System.out.println(d);


    }
}
